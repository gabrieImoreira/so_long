#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	size_t	i;

	i = ft_strlen(str) + 1;
	while (i--)
	{
		if (str[i] == (unsigned char)c)
			return ((char *)str + i);
	}
	return (NULL);
}
